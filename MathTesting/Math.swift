import Foundation

class Math{
    func add(num1: Int, num2: Int) -> Int {
        return num1 + num2
    }
    
    func sub(num1: Int, num2: Int) -> Int {
        return num1 - num2
    }
    
    func multiply(num1: Int, num2: Int) -> Int {
        return num1 * num2
    }
    
    func divide(num1: Int, num2: Int) -> Int {
        return num1 / num2
    }
}
